# Mahir scored 40, 70, 50 and 60 out of 100 in maths, science, Bangla and English. Find the percentage he got?

total = 100 * 4

math = 40
science = 70
bangla = 50
english = 60

sum = math + science + bangla + english
print('total score :', sum)

percen = (sum / total) *100
print("percantage :" , round(percen , 2) , "%")